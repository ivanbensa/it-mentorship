const b = 85;

if (b >= 0 && b <= 50) {
    console.log("Grade 5");
} else if (b >= 51 && b <= 60) {
    console.log("Grade 6");
} else if (b >= 61 && b <= 70) {
    console.log("Grade 7");
} else if (b >= 71 && b <= 80) {
    console.log("Grade 8");
} else if (b >= 81 && b <= 90) {
    console.log("Grade 9");
} else if (b >= 91 && b <= 100) {
    console.log("Grade 10");
} else {
    console.log("Points must be between 0 and 100.");
}
