const grade = 2;

if (grade === 1) {
    console.log("Fail");
} else if (grade === 2) {
    console.log("Sufficient");
} else if (grade === 3) {
    console.log("Good");
} else if (grade === 4) {
    console.log("Very good");
} else if (grade === 5) {
    console.log("Excellent");
} else {
    console.log("Grade must be between 1 and 5");
}